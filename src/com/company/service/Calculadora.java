package com.company.service;

public class Calculadora {

    public Integer somarDoisNumeros(int valorA, int valorB){
        return valorA + valorB;
    }

    public int subtrairDoisNumeros(int valorA, int valorB) {
        return  valorA - valorB;
    }

    public int deveDividirDoisNumeros(int valorA, int valorB) {
        return  valorA/valorB;
    }

    public int deveMultiplicarDoisNumeros(int valorA, int valorB) {
        return valorA * valorB;
    }
}
